// Why Use Modules

// Http => respond.js , require.js

// Encapsulation: Modules allow you to encapsulate related code into a single file or directory
// making it easier to organize and maintain your codebase. By hiding implementation details behind a module's interface,
// you can make your code more modular and easier to reason about

// Code reuse: Modules can be reused across multiple applications or projects
// reducing the amount of duplicate code you need to write. By breaking your code down into smaller
// reusable modules, you can create a library of functionality that can be shared and leveraged across your entire organization.

// Dependency management: By using modules to manage dependencies
// you can easily install and update third-party packages that your application depends on.
// This allows you to stay up-to-date with the latest security patches and bug fixes
// and makes it easier to manage the versioning and compatibility of your dependencies

// Performance: Node.js modules can improve the performance of your application
// by reducing the amount of code that needs to be loaded and executed at runtime.
// By breaking your application down into smaller, more focused modules
// you can reduce the amount of overhead and improve the speed and efficiency of your code

// Collaboration: Modules make it easier to collaborate with other developers by providing a clear interface
// and API for interacting with your code. By creating well-defined modules with clear boundaries and responsibilities,
// you can make it easier for other developers to understand and work with your code

// Security: Modules can improve the security of your application by enforcing clear boundaries
// between different parts of your code. By isolating sensitive code and data within separate modules,
// you can reduce the risk of security vulnerabilities and make it easier to audit and maintain your code